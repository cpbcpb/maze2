Player.prototype.move = function(whichKey) {
  ////erases old player location
  ctx.clearRect(this.x, this.y, this.width, this.height);
  switch (whichKey) {
    case "ArrowLeft":
        this.moveCounter +=1;
      if (this.canIMove(this.x - this.speed, this.y)) {
        this.yArray.push(this.y)
        this.xArray.push(this.x)
        this.x -= this.speed;
      }
      break;
    case "ArrowRight":
       this.moveCounter +=1;
    if (this.canIMove(this.x + this.speed, this.y)) {
        this.x += this.speed;
        this.yArray.push(this.y)
        this.xArray.push(this.x)

      }
      break;
    case "ArrowUp":
        this.moveCounter +=1;
      if (this.canIMove(this.x, this.y - this.speed)) {
        this.y -= this.speed;
        this.yArray.push(this.y)
        this.xArray.push(this.x)
        this.moveCounter +=1;
      }
      break;
    case "ArrowDown":
        this.moveCounter +=1;
      if (this.canIMove(this.x, this.y + this.speed)) {
        this.y += this.speed;
        this.yArray.push(this.y)
        this.xArray.push(this.x)
        this.moveCounter +=1;
      }
  }
  //draws image at new location
  ctx.drawImage(playerImage, this.x, this.y, this.width, this.height);
};
